public class CalculaAreaTrapezio {

    public static void main(String[] args) {


        double b, h, B, area;
        h = 5.0;
        b = 6.0;
        B = 8.0;

        area = (b + B) / 2.0 * h;


        System.out.println(area);


    }
}
